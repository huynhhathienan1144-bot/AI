def is_main_diagonal_increasing(matrix):
    n = len(matrix)
    if n <= 1: return True # A 0x0 or 1x1 matrix has an 'increasing' diagonal
    if not all(len(row) == n for row in matrix): raise ValueError("Matrix must be square")

    for i in range(n - 1):
        if matrix[i][i] >= matrix[i+1][i+1]: # Check for strictly increasing
            return False
    return True

