def merge_arrays_with_min(arr1, arr2):
    merged_arr = []
    len1 = len(arr1)
    len2 = len(arr2)
    min_len = min(len1, len2)

    # Merge common parts by taking the minimum
    for i in range(min_len):
        merged_arr.append(min(arr1[i], arr2[i]))

    # Append remaining elements from the longer array (if any)
    if len1 > len2:
        merged_arr.extend(arr1[min_len:])
    elif len2 > len1:
        merged_arr.extend(arr2[min_len:])

    return merged_arr

