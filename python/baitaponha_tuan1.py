def sum_upper_antidiagonal_triangle(matrix):
    n = len(matrix)
    if n == 0: return 0
    if not all(len(row) == n for row in matrix): raise ValueError("Matrix must be square")

    total_sum = 0
    for i in range(n):
        for j in range(n):
            # Condition for upper triangle of anti-diagonal (including anti-diagonal)
            # For anti-diagonal, i + j == n - 1
            # For upper triangle, i + j <= n - 1
            if i + j <= n - 1:
                total_sum += matrix[i][j]
    return total_sum

