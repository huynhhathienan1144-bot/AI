def check_all_even(matrix):
    rows = len(matrix)
    if rows == 0: return True # An empty matrix can be considered 'all even'
    cols = len(matrix[0])
    if cols == 0: return True # An empty matrix can be considered 'all even'

    for i in range(rows):
        for j in range(cols):
            if matrix[i][j] % 2 != 0: # If any element is odd
                return False
    return True

