def is_anti_diagonal_decreasing(matrix):
    n = len(matrix)
    if n <= 1: return True # A 0x0 or 1x1 matrix has a 'decreasing' anti-diagonal
    if not all(len(row) == n for row in matrix): raise ValueError("Matrix must be square")

    # Extract anti-diagonal elements
    anti_diagonal = [matrix[i][n - 1 - i] for i in range(n)]

    # Check if the anti-diagonal is strictly decreasing
    for i in range(len(anti_diagonal) - 1):
        if anti_diagonal[i] <= anti_diagonal[i+1]:
            return False
    return True

# Test cases
