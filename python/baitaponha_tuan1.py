def convert_negatives_to_absolute(matrix):
    # Create a new matrix to store the result to avoid modifying the original in-place
    rows = len(matrix)
    if rows == 0: return []
    cols = len(matrix[0])

    new_matrix = []
    for i in range(rows):
        new_row = []
        for j in range(cols):
            new_row.append(abs(matrix[i][j]))
        new_matrix.append(new_row)
    return new_matrix

