def is_symmetric_matrix(matrix):
    n = len(matrix)
    if n == 0: return True # An empty matrix is symmetric
    if not all(len(row) == n for row in matrix): raise ValueError("Matrix must be square")

    for i in range(n):
        for j in range(i + 1, n): # Only check upper triangle to avoid redundant checks
            if matrix[i][j] != matrix[j][i]:
                return False
    return True

