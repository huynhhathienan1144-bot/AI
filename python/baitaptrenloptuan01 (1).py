def tim_dong_tong_lon_nhat(mang):
    """Tìm dòng có tổng lớn nhất."""
    if not mang:
        print("Mảng rỗng.")
        return None, None

    max_sum = -float('inf')
    dong_max = -1
    for i, row in enumerate(mang):
        current_sum = sum(row)
        if current_sum > max_sum:
            max_sum = current_sum
            dong_max = i
    print(f"Dòng có tổng lớn nhất là dòng {dong_max} với tổng: {max_sum}")
    return dong_max, max_sum
# cau14e: Tìm cột có tích nhỏ nhất.

def tim_cot_tich_nho_nhat(mang):
    """Tìm cột có tích nhỏ nhất."""
    if not mang or not mang[0]:
        print("Mảng rỗng hoặc cột rỗng.")
        return None, None

    n_cols = len(mang[0])
    min_product = float('inf')
    cot_min = -1

    for j in range(n_cols):
        current_product = 1
        for i in range(len(mang)):
            current_product *= mang[i][j]

        if current_product < min_product:
            min_product = current_product
            cot_min = j

    print(f"Cột có tích nhỏ nhất là cột {cot_min} với tích: {min_product}")
    return cot_min, min_product
