class SinhVien:
    def __init__(self, ma_sinh_vien, ten, nam_sinh, diem_trung_binh):
        if len(ma_sinh_vien) != 10:
            raise ValueError("Mã sinh viên phải có 10 ký tự.")
        if len(ten) > 20:
            raise ValueError("Tên không được vượt quá 20 ký tự.")

        self.ma_sinh_vien = ma_sinh_vien
        self.ten = ten
        self.nam_sinh = nam_sinh
        self.diem_trung_binh = diem_trung_binh

    def __str__(self):
        return f"Mã SV: {self.ma_sinh_vien}, Tên: {self.ten:<20}, Năm sinh: {self.nam_sinh}, ĐTB: {self.diem_trung_binh}"


