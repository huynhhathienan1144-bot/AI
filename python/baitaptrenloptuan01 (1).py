def tinh_tb_phan_tu_bien(mang):
    """Tính trung bình cộng các phần tử thuộc biên."""
    if not mang or not mang[0]:
        print("Mảng rỗng.")
        return 0

    m = len(mang)
    n = len(mang[0])

    if m == 1 and n == 1:
        print(f"Trung bình cộng các phần tử thuộc biên: {mang[0][0]}")
        return mang[0][0]

    total_sum = 0
    count = 0

    # Dòng đầu và dòng cuối
    for j in range(n):
        total_sum += mang[0][j]
        count += 1
        if m > 1:
            total_sum += mang[m-1][j]
            count += 1

    # Cột đầu và cột cuối (trừ các phần tử đã tính ở dòng đầu/cuối)
    for i in range(1, m - 1):
        total_sum += mang[i][0]
        count += 1
        if n > 1:
            total_sum += mang[i][n-1]
            count += 1

    # Loại bỏ các phần tử trùng lặp nếu m > 1 và n > 1
    if m > 1 and n > 1:
        # Bốn góc được tính 2 lần, cần trừ đi 4
        # Hoặc cách đơn giản hơn: tính riêng từng cạnh và đảm bảo không trùng lặp
        # Ví dụ: (mang[0][0], mang[0][n-1], mang[m-1][0], mang[m-1][n-1])
        # đã được tính hai lần. Tuy nhiên, cách trên có thể tính được.
        # Cách an toàn hơn là dùng set để lưu các vị trí hoặc đảm bảo logic không trùng lặp

        # Reset và tính lại cẩn thận hơn để tránh trùng lặp đếm
        total_sum = 0
        count = 0

        # Dòng đầu
        for j in range(n):
            total_sum += mang[0][j]
            count += 1

        # Dòng cuối (nếu có hơn 1 dòng)
        if m > 1:
            for j in range(n):
                total_sum += mang[m-1][j]
                count += 1

        # Cột đầu (trừ 2 phần tử góc đã tính)
        for i in range(1, m - 1):
            total_sum += mang[i][0]
            count += 1

        # Cột cuối (nếu có hơn 1 cột, trừ 2 phần tử góc đã tính)
        if n > 1:
            for i in range(1, m - 1):
                total_sum += mang[i][n-1]
                count += 1


    if count > 0:
        average = total_sum / count
        print(f"Trung bình cộng các phần tử thuộc biên: {average}")
        return average
    else:
        print("Không có phần tử nào thuộc biên.")
        return 0
