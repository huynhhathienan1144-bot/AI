def tinh_tb_chan_dong_le(mang):
    """Tính trung bình cộng các phần tử chẵn thuộc dòng lẻ."""
    if not mang or not mang[0]:
        print("Mảng rỗng.")
        return 0

    total_sum = 0
    count = 0
    for i in range(len(mang)):
        if i % 2 != 0:  # Dòng lẻ (chỉ số 1, 3, 5, ...)
            for j in range(len(mang[0])):
                if mang[i][j] % 2 == 0:  # Phần tử chẵn
                    total_sum += mang[i][j]
                    count += 1

    if count > 0:
        average = total_sum / count
        print(f"Trung bình cộng các phần tử chẵn thuộc dòng lẻ: {average}")
        return average
    else:
        print("Không có phần tử chẵn nào thuộc dòng lẻ.")
        return 0
